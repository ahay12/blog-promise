<script>
import { ref } from "vue";
import { useRouter } from "vue-router";
import { db } from "../../firebase/config";
import { addDoc, collection } from "firebase/firestore";

export default {
  setup() {
    const router = useRouter();
    const title = ref("");
    const body = ref("");
    const tag = ref("");
    const tags = ref([]);

    const handleKeyDown = () => {
      const trimmedTag = tag.value.trim();

      if (trimmedTag && !tags.value.includes(trimmedTag) && tags.value.length < 5) {
        // trimmedTag = tag.value.replace(/\s/g,'')
        tags.value.push(trimmedTag);
      }

      tag.value = "";
    };

    const handleSubmit = async () => {
      const post = {
        title: title.value,
        body: body.value,
        tags: tags.value,
      };

      const res = await addDoc(collection(db, "posts"), post);

      // router.push({ name: "Home" });
    };

    return { title, body, tag, tags, handleKeyDown, handleSubmit };
  },
};
</script>

